int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int j;

	i = 0;
	j = 1;
	while (i < length)
	{
		j = i + 1;
		if (f(tab[i], tab[j]) > 0)
			return (0);
			i++;
	}
	return (1);
}