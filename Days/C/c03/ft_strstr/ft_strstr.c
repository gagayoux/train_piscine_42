char	*ft_strstr(char *str, char *to_find)
{
	int		i;
	int		j;
	char	*ptr;

	i = 0;
	j = 0;
	ptr = &to_find[0];
	while (str[i] != '\0')
	{
		if (to_find[j] == str[i])
			return (ptr);
		i++;
	}
	return ;
}
